drop table tb_user IF EXISTS;
CREATE TABLE `tb_user` (
  `id` int(11) NOT NULL AUTO_INCREMENT COMMENT 'ID',
  `email` varchar(255) COLLATE utf8_bin DEFAULT NULL COMMENT '이메일(아이디)',
  `company` varchar(255) COLLATE utf8_bin DEFAULT NULL COMMENT '회사명',
  `address` varchar(255) COLLATE utf8_bin DEFAULT NULL COMMENT '주소',
  `pw` varchar(128) COLLATE utf8_bin DEFAULT NULL COMMENT '비밀번호',
  `salt` varchar(128) COLLATE utf8_bin DEFAULT NULL COMMENT 'saltKey',
  `username` varchar(20) COLLATE utf8_bin DEFAULT NULL COMMENT '이름',
  `phone` varchar(30) COLLATE utf8_bin DEFAULT NULL COMMENT '전화',
  `gender` varchar(1) COLLATE utf8_bin DEFAULT 'M' COMMENT '성별',
  `user_type` char(1) COLLATE utf8_bin NOT NULL DEFAULT 'U' COMMENT '사용자구분',
  `refresh_token` varchar(255) COLLATE utf8_bin DEFAULT NULL COMMENT 'refresh token',
  `login_fail_cnt` tinyint(1) unsigned DEFAULT 0 COMMENT '로그인실패횟수',
  `pw_chg_dt` datetime DEFAULT NULL COMMENT '패스워드변경일시',
  `created_dt` datetime DEFAULT current_timestamp(),
  `updated_dt` datetime DEFAULT NULL,
  `lock_yn` char(1) COLLATE utf8_bin DEFAULT 'N' COMMENT '잠김여부',
  `del_yn` char(1) COLLATE utf8_bin DEFAULT 'N' COMMENT '삭제여부',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uq_user_email` (`email`)
) ENGINE=InnoDB AUTO_INCREMENT=61 DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='유저기본정보';

drop table tb_file IF EXISTS;
CREATE TABLE `tb_file` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT,
  `filepath` varchar(500) DEFAULT NULL,
  `filename` varchar(255) DEFAULT NULL,
  `orgname` varchar(255) DEFAULT NULL,
  `ext` varchar(128) DEFAULT NULL,
  `created_dt` datetime DEFAULT current_timestamp(),
  `updated_dt` datetime DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=45 DEFAULT CHARSET=utf8;

